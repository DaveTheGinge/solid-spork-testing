// Random X Between Width Of My Game
function randomX () {
    let randomX = Math.floor(Math.random() * ( 250 - 100) + 100);
    return randomX;
}

// Random Y Between Height Of My Game
function randomY () {
    let randomY = Math.floor(Math.random() * (700 - 100) + 100);
    return randomY;
}

